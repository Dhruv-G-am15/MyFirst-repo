#include<iostream>
using namespace std;

int main() 
{
    int a,b,sum,sub,div,mult;
    

    cout <<"Enter the value of a: ";
    cin >>a;

    cout <<"Enter the value of b: ";
    cin >>b;

    sum=a+b;
    cout <<"Sum is: "<<sum<<endl;

    sub=a-b;
    cout<<"Sub is: "<<sub<<endl;

    div=a/b;
    cout<<"Div is: "<<div<<endl;

    mult=a*b;
    cout<<"Mult is: "<<mult<<endl;
    
    return 0;
}